colors = [
    (1, 0, 0),   # rojo
    (0, 1, 0),   # verde
    (0, 0, 1),   # azul
    (1, 1, 0),   # amarillo
    (1, 0, 1),   # magenta
    (0, 1, 1),   # cian
    (1, 0.5, 0), # naranja
    (0.5, 0, 1), # violeta
] * 3


def wireCube():
    for i in range(0, len(triangles), 3):
        glBegin(GL_TRIANGLES)
        for j in range(3):
            idx = triangles[i + j]
            glColor3fv(colors[idx % len(colors)]) 
            glVertex3fv(vertices[idx])
        glEnd()
